    private void HapusLayar(){
        txtusername.setText("");
        txtPassword.setText("");
        txtusername.setEnabled(true);
        txtPassword.setEnabled(true);
     }

    private void Login(){
        try{
            if(txtusername.getText().equals("") ||
                txtPassword.getText().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Username  Password Tidak Boleh Kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
                txtusername.requestFocus(); 
                HapusLayar();
            }else{
                st = conn.createStatement();
            String sql = "SELECT * FROM admin WHERE username ='"+txtusername.getText()+"' AND password ='"
                    +String.valueOf(txtPassword.getText())+"'";
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
                this.dispose();
                new MainFormStore().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Username atau Password Salah", "Pesan",
                JOptionPane.ERROR_MESSAGE);
                HapusLayar();
            }
            }
        }catch(Exception e){
        e.printStackTrace();

    }
    }
